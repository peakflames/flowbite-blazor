namespace Flowbite.Components;

/// <summary>
/// Defines the available color variants for the TextInput component.
/// </summary>
public enum TextInputColor
{
    /// <summary>
    /// Default gray color scheme.
    /// </summary>
    Gray,

    /// <summary>
    /// Info color scheme (blue/cyan).
    /// </summary>
    Info,

    /// <summary>
    /// Success color scheme (green).
    /// </summary>
    Success,

    /// <summary>
    /// Failure color scheme (red).
    /// </summary>
    Failure,

    /// <summary>
    /// Warning color scheme (yellow).
    /// </summary>
    Warning
}
