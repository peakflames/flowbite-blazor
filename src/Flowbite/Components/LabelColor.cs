namespace Flowbite.Components;

/// <summary>
/// Defines the available color variants for the Label component.
/// </summary>
public enum LabelColor
{
    /// <summary>
    /// Default label color with gray text.
    /// </summary>
    Default,

    /// <summary>
    /// Info label color with cyan text.
    /// </summary>
    Info,

    /// <summary>
    /// Success label color with green text.
    /// </summary>
    Success,

    /// <summary>
    /// Failure label color with red text.
    /// </summary>
    Failure,

    /// <summary>
    /// Warning label color with yellow text.
    /// </summary>
    Warning
}
