namespace Flowbite.Components;

/// <summary>
/// Defines the available color variants for the Checkbox component.
/// </summary>
public enum CheckboxColor
{
    /// <summary>
    /// Default checkbox color with primary theme color.
    /// </summary>
    Default,

    /// <summary>
    /// Primary color variant.
    /// </summary>
    Primary,

    /// <summary>
    /// Success color variant (green).
    /// </summary>
    Success,

    /// <summary>
    /// Warning color variant (yellow).
    /// </summary>
    Warning,

    /// <summary>
    /// Failure color variant (red).
    /// </summary>
    Failure,

    /// <summary>
    /// Info color variant (blue).
    /// </summary>
    Info
}
