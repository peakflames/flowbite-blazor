namespace Flowbite.Components;

/// <summary>
/// Defines the size options for a dropdown component.
/// </summary>
public enum DropdownSize
{
    /// <summary>
    /// Small dropdown size.
    /// </summary>
    Small,

    /// <summary>
    /// Medium dropdown size (default).
    /// </summary>
    Medium,

    /// <summary>
    /// Large dropdown size.
    /// </summary>
    Large
}

/// <summary>
/// Defines the placement options for a dropdown menu.
/// </summary>
public enum DropdownPlacement
{
    /// <summary>
    /// Dropdown menu positioned at the bottom of the trigger (default).
    /// </summary>
    Bottom,

    /// <summary>
    /// Dropdown menu positioned at the top of the trigger.
    /// </summary>
    Top,

    /// <summary>
    /// Dropdown menu positioned to the right of the trigger.
    /// </summary>
    Right,

    /// <summary>
    /// Dropdown menu positioned to the left of the trigger.
    /// </summary>
    Left,

    /// <summary>
    /// Dropdown menu positioned at the bottom-start of the trigger.
    /// </summary>
    BottomStart,

    /// <summary>
    /// Dropdown menu positioned at the bottom-end of the trigger.
    /// </summary>
    BottomEnd,

    /// <summary>
    /// Dropdown menu positioned at the top-start of the trigger.
    /// </summary>
    TopStart,

    /// <summary>
    /// Dropdown menu positioned at the top-end of the trigger.
    /// </summary>
    TopEnd,

    /// <summary>
    /// Dropdown menu positioned at the right-start of the trigger.
    /// </summary>
    RightStart,

    /// <summary>
    /// Dropdown menu positioned at the right-end of the trigger.
    /// </summary>
    RightEnd,

    /// <summary>
    /// Dropdown menu positioned at the left-start of the trigger.
    /// </summary>
    LeftStart,

    /// <summary>
    /// Dropdown menu positioned at the left-end of the trigger.
    /// </summary>
    LeftEnd
}
